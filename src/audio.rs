    fn detect_from_system_falls_back_gracefully() {
        // On macOS or any system without /proc/asound, this should fall back
        // to default_safe() without panicking.
        let config = AudioPassthrough::detect_from_system();
        // Should at minimum have AC3 + DTS (from fallback)
        assert!(config.ac3);
        assert!(config.dts);
    }
